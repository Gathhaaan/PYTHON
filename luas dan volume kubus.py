#minta input user
sisi = int(input("berapa sisi kubus ="))

#membuat rumus luas dan volume kubus
hitung_luas_kubus = 6 * sisi ** 2
hitung_volume_kubus = sisi ** 3

#menghitung dan menampilkan hasil
print(f"luas kubus dengan sisi-sisi {sisi} adalah {hitung_luas_kubus}")
print(f"volume kubus dengan sisi-sisi {sisi} adalah {hitung_volume_kubus}")



